    <h1 class="titulo">Vacina da Pfizer BioNTech contra a COVID-19: Aquilo que precisa de saber</h1>
    <hr>

    <h2>Quem deve ser vacinado em primeiro lugar?</h2>

    <p class="texto">Embora o abastecimento de vacinas seja limitado, recomenda-se que seja dada prioridade aos profissionais de saúde que estejam em alto risco de exposição e às pessoas idosas, incluindo as que têm 65 anos ou mais.Os países poderão consultar o <a href="https://iris.paho.org/handle/10665.2/53580#:~:text=Este%20Roteiro%20baseia%2Dse%20no,priorizado%20devido%20%C3%A0%20oferta%20limitada.">Roteiro de Priorização da OMS</a> e o <a href="https://www.who.int/publications/i/item/who-sage-values-framework-for-the-allocation-and-prioritization-of-covid-19-vaccination">Quadro de Valores da OMS</a> como orientação para a priorização dos seus grupos-alvo</p>

    <h2>Quem mais deve tomar a vacina?</h2>

    <p class="texto">Está demonstrado que a vacina é segura e eficaz em pessoas com várias patologias associadas a maior risco de doença grave.</p>
    <p class="texto">Entre elas contam-se a hipertensão, diabetes, asma, doença pulmonar, doença hepática ou renal, assim como infeções crónicas que estejam estáveis e controladas.</p>
    <p class="texto">É necessário fazer mais estudos sobre o impacto em pessoas imunocomprometidas. A recomendação provisória é que as pessoas imunocomprometidas que façam parte de um grupo recomendado para vacinação possam ser vacinadas, embora só quando for possível e não antes de receberem informação e aconselhamento.</p> 
    <p class="texto">As pessoas que vivem com o VIH correm maior risco de doença grave de COVID-19. Os dados dos ensaios clínicos sobre a segurança nas pessoas VIH-positivas com doença bem controlada são limitados. Os destinatários da vacina que sejam seropositivos para o VIH devem ser informados e, quando possível, aconselhados em função dos dados disponíveis.</p>
    <p class="texto">A vacinação pode ser oferecida a pessoas que já tenham tido COVID-19 no passado. Mas, dada a limitação do abastecimento de vacinas, a vacinação contra a COVID-19 dessas pessoas poderá ser adiada até seis meses desde o momento da sua infeção pelo SARS-CoV-2.</p>
    <p class="texto">A vacina anda não foi estudada em mulheres lactantes, mas não se tratando de uma vacina com vírus vivo, o mRNA não entra no núcleo da célula e degrada-se rapidamente, não podendo, por isso, interferir com as funções celulares.</p>
    <p class="texto">Se uma mulher lactante fizer parte de um grupo (por ex., profissional de saúde) recomendado para vacinação, poderá ser vacinada. O SAGE não recomenda a suspensão da amamentação depois da vacinação. Estão ser estudadas mais evidências, para fundamentar melhor as recomendações de política da OMS sobre este assunto.</p>

    <h2>As mulheres grávidas devem ser vacinadas?</h2>

    <p class="texto">Embora a gravidez coloque as mulheres em maior risco de COVID-19 grave, são muito escassos os dados disponíveis para avaliar a segurança da vacina na gravidez. </p>
    <p class="texto">As mulheres grávidas podem receber a vacina, se o benefício  da sua vacinação superar os potenciais riscos da vacina.</p>
    <p class="texto">Por essa razão, as mulheres grávidas em alto risco de exposição ao SARS-CoV-2 (por ex., as profissionais de saúde) ou que tenham comorbilidades que possam agravar o seu risco de doença grave, podem ser vacinadas, em consulta com o seu prestador de cuidados de saúde.</p>

    <h2> A quem não é recomendada a vacina?</h2>

    <p class="texto">As pessoas que tenham história de reação alérgica grave a qualquer componente da vacina não a devem tomar.</p>
    <p class="texto">A vacina apenas foi testada em crianças  com mais de 16 anos de idade. Por conseguinte, neste momento, a OMS não recomenda a vacinação a crianças menores de 16 anos, ainda que pertençam a um grupo de alto risco.</p>

    <h2>Qual a dosagem recomendada?</h2>

    <p class="texto">O efeito de proteção começa a desenvolver-se 12 dias após a toma da primeira dose, mas a proteção integral requer duas doses, que a OMS recomenda que sejam inoculadas com um intervalo de 21 a 28 dias. É necessário pesquisar mais para se compreender o potencial de proteção a mais longo prazo depois de uma dose única.   </p>

    <h2>A vacina é segura?</h2>

    <p class="texto">A OMS concedeu autorização para utilização de emergência da vacina da Pfizer BioNTech em 31 de Dezembro de 2020. A OMS avaliou exaustivamente a qualidade, segurança e eficácia da vacina e recomendou a sua utilização em pessoas com mais de 16 anos.</p>
    <p class="texto">O Comité Consultivo Mundial para a Segurança das Vacinas, um grupo de peritos que apresenta orientações independentes e fundamentadas à OMS sobre a questão da utilização segura das vacinas, recebe e avalia relatórios de eventos de segurança suspeitos com potencial impacto internacional. </p>

    <h2>Qual a eficácia da vacina?</h2>

    <p class="texto">A vacina da Pfizer BioNTech contra a COVID-19 tem uma eficácia de 95% contra a infeção sintomática por SARS-CoV-2.</p>

    <h2> A vacina funciona contra as novas variantes?</h2>

    <p class="texto">O SAGE analisou todos os dados disponíveis sobre o desempenho da vacina em testes para avaliar a sua eficácia contra um certo número de variantes. Esses testes indicaram que a vacina é eficaz contra as variantes do vírus.</p>
    <p class="texto">Presentemente, o SAGE recomenda a utilização da vacina da Pfizer BioNTech de acordo com o Roteiro de Priorização da OMS, mesmo que haja variantes do vírus num determinado país. Os países devem avaliar os riscos e os benefícios, tendo em consideração a sua situação epidemiológica.</p>
    <p class="texto">Os resultados preliminares salientam a urgente necessidade de uma abordagem coordenada para a vigilância e avaliação das variantes e seu potencial impacto na eficácia da vacina. À medida que forem disponibilizados novos dados, a OMS atualizará as suas recomendações em conformidade. </p>

    <h2>A vacina evita a infeção e a transmissão?</h2>

    <p class="texto">Presentemente, não existem dados substantivos relacionados com o impacto da vacina da Pfizer BioNTech sobre a transmissão ou disseminação viral.</p>
    <p class="texto">Entretanto, devemos manter e reforçar as medidas de saúde pública que funcionam: uso de máscara, distanciamento físico, lavagem das mãos, etiqueta respiratória e da tosse, afastamento de multidões e boa ventilação em espaços fechados.</p>

    <h2>E sobre outras vacinas em desenvolvimento para combater a COVID-19?</h2>

    <p class="texto">A OMS não faz, normalmente, recomendações para vacinas específicas, mas faz uma recomendação que abrange todas as vacinas para uma determinada doença, a menos que as evidências sugiram que é necessária uma abordagem diferente.</p>
    <p class="texto">Devido à grande variedade de vacinas contra a COVID-19 baseadas em tecnologias de plataformas muito diferentes, a OMS está a analisar as vacinas, à medida que elas são autorizadas por autoridades reguladoras nacionais altamente competentes e que estejam disponíveis em quantidades suficientes para responder às necessidades de muitos países.</p>
    <p class="texto">A OMS não tem preferência por nenhum dos produtos e a variedade de produtos, incluindo os seus atributos específicos e requisitos de manuseamento, permitem aos países selecionarem os produtos que sejam mais adequados às suas circunstâncias.</p>
    <p class="texto">Está previsto que o SAGE da OMS examine outras vacinas nos próximos meses. </p>

