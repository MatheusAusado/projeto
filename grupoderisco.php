    <h1 class="titulo">2.SE FAÇO PARTE DO GRUPO DE RISCO, COMO POSSO ME PROTEGER?</h1>

    <p class="texto">Nesse caso, precisa ficar ainda mais atento com <strong>higiene pessoal</strong> e <strong>cuidados gerais</strong>:</p>

    <ul class="lista">
        <li>lavar as mãos com água e sabão frequentemente. O tempo de lavagem deve ser de, pelo menos, 20 segundos;</li>
        <li>evitar encostar na boca, no rosto e no nariz;</li>
        <li>evitar, sempre que possível, contato físico com outras pessoas e aglomerações;</li>
        <li>não sair de casa com frequência;</li>
        <li>caso precise sair de casa, usar máscaras faciais ajustadas (com, pelo menos, duas camadas de tecido), cobrindo boca e nariz.</li>
    </ul>

    <p class="texto">Caso apresente algum sintoma gripal (febre, tosse, nariz escorrendo e dor de garganta), observe sinais de alarme: diarreia e vômitos persistentes, falta de ar e febre com duração superior a 48 horas. Nesses casos, busque o atendimento em uma Unidade de Pronto Atendimento. Antes disso, você também pode fazer uma autoavaliação ou atendimento à distância no <a href="https://coronavirus.saude.mg.gov.br/saudedigitalmg" class="q">Saúde Digital</a>.</p>

    <h1 class="titulo">3.PESSOAS QUE FAZEM USO DE REMÉDIOS TODOS OS DIAS DEVEM SUSPENDÊ-LOS?</h1>
   
    <p class="texto">Não. Quem faz uso de qualquer medicação devido a outras doenças, deve manter o uso conforme a orientação do seu médico. Caso a doença não seja  adequadamente tratada, deverá agendar uma consulta para ajustar os remédios utilizados. Nesse momento de pandemia, é fundamental o controle correto de doenças crônicas como pressão alta, diabetes e asma.</p>
   
    <h1 class="titulo">4.AS PESSOAS COM DEFICICIÊNCIA FAZEM PARTE DO GRUPO DE RISCO?</h1>
   
    <p class="texto">Como um grupo geral, pacientes com deficiências físicas e/ou intelectuais não fazem parte do grupo de risco para formas graves da covid-19. Porém, uma pessoa com deficiência E outras doenças já classificadas (problemas no pulmão, no coração, nos rins, no fígado etc.) se enquadram no grupo de risco.Caso seja portador de alguma deficiência e apresente sintomas gripais (febre, tosse, nariz escorrendo e dor de garganta), deve conversar com um profissional de saúde. Por meio do Saúde Digital, um aplicativo do Governo de Minas Gerais, os mineiros podem fazer uma consulta à distância de alta qualidade.</p>

    <h1 class="titulo">5.PESSOAS COM APENAS PRESSÃO ALTA TÊM MAIOR RISCO DE DESENVOLVER FORMAS GRAVES DA COVID-19?</h1>

    <p class="texto">Acredita-se que a hipertensão não seja fator de risco para pior evolução da covid-19. Porém, é muito frequente a associação da pressão alta com outras doenças, como diabetes e problemas no coração. Por causa dessa associação com outras doenças, pode haver maior risco para formas graves da covid-19. Essas condições podem ser silenciosas e, por isso, a prevenção é tão importante.</p>
    <p class="texto">Agora que você já sabe as principais informações sobre os grupos de risco e que provavelmente tirou suas dúvidas sobre o novo coronavírus e a covid-19, não deixe de redobrar sua atenção caso você ou algum conhecido faça parte do grupo de risco. Tenha sempre a prevenção em dia: mantenha o distanciamento social, a higienização das mãos e de superfícies e utilize máscaras!</p>
    

